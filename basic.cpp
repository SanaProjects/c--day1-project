#include<iostream>
using namespace std;

int main()

{
    int a,b,c,d,e,f,g;

    cout<<endl<<"Enter value in a:";
    cin>>a;

    cout<<endl<<"Enter value in b:";
    cin>>b;

    c=a+b;
    d=a-b;
    e=a*b;
    f=a/b;
    g=a%b;


    cout<<endl<<"Addition ="<<c;
    cout<<endl<<"Substraction ="<<d;
    cout<<endl<<"Multiplication ="<<e;
    cout<<endl<<"Division ="<<f;
    cout<<endl<<"Remainder ="<<g;

    return 0;
}


